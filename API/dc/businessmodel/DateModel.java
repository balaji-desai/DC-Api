package dc.businessmodel;

public class DateModel {
private int Day;
private int Month;
private int Year;
public int getDay() {
	return Day;
}
public int getMonth() {
	return Month;
}
public int getYear() {
	return Year;
}
public void setDay(int day) {
	Day = day;
}
public void setMonth(int month) {
	Month = month;
}
public void setYear(int year) {
	Year = year;
}
}
